


'''================================================================================
     IF Statement, ELIF Statement, ELSE Statement
================================================================================'''

swapnil = int(input("Enter any number : "))

if swapnil > 10:                         #-----------------------------> Condition
    print (f"Number {swapnil} is greater")   # -----------------------> This is IF BLOCK
    print("Number", swapnil , "is greater")

elif swapnil == 10:
    print(f"Number is Equal to {swapnil}")
    print("Number is Equal to ", swapnil) # -----------------------> This is ELIF BLOCK

else:
    print(f"{swapnil} Number is Smaller")
    print(swapnil, "Number is Smaller") # -----------------------> This is ELSE BLOCK










